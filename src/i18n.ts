export const MESSAGES = {
  en: MESSAGES_EN,
  ru: MESSAGES_RU,
} as const;

export type I18nKey = keyof typeof MESSAGES_EN;

export function resolveLanguage(language?: string): Locale {
  const value = (language ?? getLanguage()).toLowerCase();
  return value.startsWith("ru") ? "ru" : "en";
}

export function t(key: I18nKey, vars?: MessageVars): string {
  const locale = resolveLanguage();
  let message = MESSAGES[locale][key] ?? MESSAGES.en[key];
  if (!vars) return message;

  message = message.replace(/\{\{(\w+)\}\}/g, (_, name: string) => {
    if (!(name in vars)) return `{{${name}}}`;
    return String(vars[name]);
  });

  return message;
}
